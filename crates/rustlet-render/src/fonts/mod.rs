#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn font_has_ascii() {
        let font = get_font(DEFAULT_FONT);
        assert!(font.glyph('A').is_some());
        assert!(font.glyph(' ').is_some());
        assert!(font.glyph('~').is_some());
    }

    #[test]
    fn measure_width() {
        let font = get_font(DEFAULT_FONT);
        assert_eq!(font.measure_width("Hello"), 25); // 5 chars * 5px
        assert_eq!(font.measure_width(""), 0);
    }

    #[test]
    fn glyph_a_has_pixels() {
        let font = get_font(DEFAULT_FONT);
        let g = font.glyph('A').unwrap();
        // 'A' row 0 is 0x70 = 0111_0000 → pixels at columns 1,2,3
        assert!(g.bitmap[0] & 0x80 == 0); // col 0 off
        assert!(g.bitmap[0] & 0x40 != 0); // col 1 on
    }
}
